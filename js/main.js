const initialize = () => {
	const ethereumButton = document.querySelector('.enableEthereumButton');
	const showAccount = document.querySelector('.showAccount');
	const sendEthButton = document.querySelector('.sendEthButton');
	const mintStatus = document.querySelector('.mintStatus')

	let accounts = [];
	const isMetaMaskInstalled = () => {
		//Have to check the ethereum binding on the window object to see if it's installed
		const { ethereum } = window;
		return Boolean(ethereum && ethereum.isMetaMask);
	  };
	const MetaMaskClientCheck = () => {
    //Now we check to see if MetaMask is installed
    if (!isMetaMaskInstalled()) {
		//If it isn't installed we ask the user to click to install it
		ethereumButton.innerText = 'Please install MetaMask!';
		} else {
		//If it is installed we change our button text
		ethereumButton.innerText = 'Connect';
		}
	};
	MetaMaskClientCheck();
	async function getMMAccount() {
		const accounts = await window.ethereum.request({
		method: 'eth_requestAccounts',
		});
		const account = accounts[0];
		showAccount.innerHTML = account;
		return account
	}


	ethereumButton.addEventListener('click', () => {
		getMMAccount();
	});
	ethereum.on('accountsChanged', function (accounts) {
		// Time to reload your interface with accounts[0]!
		showAccount.innerHTML = "Account disconnected or changed."
		getMMAccount();
		
	});
	async function buy0n1() {
		let w3 = new Web3(Web3.givenProvider || "http://127.0.0.1:5500/");
		let cost = w3.utils.toWei("0.05");
		let walletAddress = await getMMAccount();
		let numberOfDoges = document.getElementById('0n1Amount').value;
		if (numberOfDoges <= 0 || numberOfDoges > 2 || isNaN(numberOfDoges)) {
			mintStatus.innerHTML = "Please set an appropriate amount to min.";
			return;
		}
		const gasPrice = await w3.eth.getGasPrice();
		const gasLimit = 250000; // gas has consistently been < 200000
		const contract = new w3.eth.Contract(abi, contractAddress, {from: walletAddress});
		const res = await contract.methods.mint(numberOfDoges).send({
			from: walletAddress,
			value: cost * numberOfDoges,
			gasPrice: gasPrice,
			gas: gasLimit * numberOfDoges
		});
		if (res.status) {
			mintStatus.innerHTML = "Minted " + numberOfDoges;
			return;
		} else {
			mintStatus.innerHTML = "Try again";
			console.log("Failed")
		}
		mintStatus.innerHTML = "Try again";
		console.log("Failed")
		return;
		
	}
	sendEthButton.addEventListener('click', () => {
		buy0n1();
	});

	
  };

window.addEventListener('DOMContentLoaded', initialize);




